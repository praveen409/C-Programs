













	// Short - Hand Assignments...

	#include<stdio.h>

	int main()
	{	
		int number = 5;

		printf("Number = %d\n", number); // 5

		number = 6;

		printf("Number = %d\n", number);  // 6

		number = number + 2;

		printf("Number = %d\n", number);  // 8

		number += 2;

		printf("Number = %d\n", number);  // 10
	
		number *= number;

		printf("Number = %d\n", number);  // 100

		return 0;
	
	}



	
