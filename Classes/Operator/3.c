




	// Increment / Decrement Operator
	#include<stdio.h>

	int main()
	{	
		int number = 5;

		printf("Number = %d\n", number); // 5


		number = number - 1;

		
		printf("Number = %d\n", number);  // 4

		number -= 1;

		printf("Number = %d\n", number); // 3


		number--;
		
		printf("Number = %d\n", number); // 2

		
		--number;

		printf("Number = %d\n", number); // 1


		return 0;
	
	}



	
